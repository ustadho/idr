    private void udfLoadData(String s){
        String query="select * from fn_rpt_kartu_tagihan_hutang("
                + "'"+fn.yyyymmdd_format.format(jXDatePicker1.getDate())+"', "
                + "'"+fn.yyyymmdd_format.format(jXDatePicker2.getDate())+"', "
                + fn.udfGetInt(txtSupplier.getText())+ ") as (id bigint, keterangan varchar, invoice_no varchar, \n" +
                    "tanggal date, invoice_amount double precision, paid_amount double precision, owing double precision, \n" +
                    "bayar double precision, tgl_bayar date, alat_bayar varchar, akun varchar, ket_pembayaran text)";
        System.out.println(query);
        ((DefaultTableModel)jXTable1.getModel()).setNumRows(0);
        try {
            ResultSet rs=conn.createStatement().executeQuery(query);
            int i=1, row=0;
            while(rs.next()){
                ((DefaultTableModel)jXTable1.getModel()).addRow(new Object[]{
                    i,
                    rs.getDate("tanggal"),
                    rs.getString("invoice_no"),
                    rs.getDouble("invoice_amount"),
                    rs.getDate("tgl_bayar"),
                    rs.getDouble("bayar"),
                    rs.getString("alat_bayar"),
                    rs.getString("akun"),
                    rs.getString("ket_pembayaran"),
                    rs.getInt("id")
                });
                i++;
            }
            rs.close();
            if(jXTable1.getRowCount() >0){
                jXTable1.setRowSelectionInterval(row, row);
                jXTable1.setModel(fn.autoResizeColWidth(jXTable1, ((DefaultTableModel)jXTable1.getModel())).getModel());
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmKartuPiutang.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
