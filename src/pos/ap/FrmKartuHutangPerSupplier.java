    private void txtSupplierKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSupplierKeyReleased
        String sQry="select id_relasi::varchar as id_customer, coalesce(nama_relasi,'') as nama_customer, "
        + "alamat||' - '||kota.nama_kota as alamat, coalesce(s.top,0) as top "
        + "from m_relasi s "
        + "left join m_kota kota on kota.id=s.id_kota "
        + "where tipe_relasi=1 and id_relasi::varchar||coalesce(nama_relasi,'')||"
        + "coalesce(alamat,'')||' - '||coalesce(kota.nama_kota,'') "
        + "ilike '%"+txtSupplier.getText()+"%' order by 2" ;
        fn.lookup(evt, new Object[]{lblCustomer}, sQry, txtSupplier.getWidth()+lblCustomer.getWidth(), 300);
    }//GEN-LAST:event_txtSupplierKeyReleased

