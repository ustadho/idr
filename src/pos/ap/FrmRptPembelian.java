    private void initForm(){
        try {
            fn.setConn(this.conn);
            service=new ReportService(conn, this);
            
            Timestamp skg=fn.getTimeServer();
            jXDatePickerAwal.setDate(skg); jXDatePickerAwal.setFormats(MainForm.sDateFormat);
            jXDatePickerAkhir.setDate(skg);jXDatePickerAkhir.setFormats(MainForm.sDateFormat);
            jList1.setSelectedIndex(0);
            RelasiDao dao=new RelasiDao(conn);
            relasiList=dao.cariSemuaData(1);
            cmbSupplier.removeAllItems();
            cmbSupplier.addItem("");
            for(Relasi r: relasiList){
                cmbSupplier.addItem(r.getNamaRelasi());
            }
            
            ItemDao daoItem=new ItemDao(conn);
            itemList=daoItem.tampilkanData("");
            cmbItem.removeAllItems();
            cmbItem.addItem("");
            for(Item i: itemList){
                cmbItem.addItem(i.getNama_barang()+" "+i.getPlu());
            }
            AutoCompleteDecorator.decorate(cmbSupplier);
            AutoCompleteDecorator.decorate(cmbItem);
            
        } catch (Exception ex) {
            Logger.getLogger(FrmRptPenjualan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
