    SimpleDateFormat fmt=new SimpleDateFormat("yyyy-MM-dd");
    
    private void udfPreview(){
        HashMap param=new HashMap();
        param.put("toko", MainForm.sToko);
        param.put("alamat1", MainForm.sAlamat1);
        param.put("alamat2", MainForm.sAlamat2);
        param.put("telp", MainForm.sTelp1);
        param.put("email", MainForm.sEmail);
        param.put("tanggal1", fmt.format(jXDatePickerAwal.getDate()));
        param.put("tanggal2", fmt.format(jXDatePickerAkhir.getDate()));
        param.put("idSupplier", cmbSupplier.getSelectedIndex()==0? null: relasiList.get(cmbSupplier.getSelectedIndex()-1).getIdRelasi() );
        param.put("namaSupplier", cmbSupplier.getSelectedIndex()==0? "": relasiList.get(cmbSupplier.getSelectedIndex()-1).getNamaRelasi());
        

        switch(jList1.getSelectedIndex()){
            case 0:{
                param.put("trxType", "PBL");
                service.udfPreview(param, "PembelianDetail", null);       break;
            }
            case 1:{
                param.put("trxType", "RBL");
                service.udfPreview(param, "PembelianDetail", null);       break;
            }
            case 2:{
                param.put("trxType", "");
                service.udfPreview(param, "PembelianDetailPerSupplier", null);       break;
            }
            case 3:{
                service.udfPreview(param, "PembelianRekapPerNota", null);        break;
            }
            case 4:{
                param.put("plu", cmbItem.getSelectedIndex()==0? "": itemList.get(cmbItem.getSelectedIndex()-1).getPlu());
                service.udfPreview(param, "PembelianDetailPerBarang", null);        break;
            }
            case 5:{
                service.udfPreview(param, "HutangSupplier", null);        break;
            }
            default:{
                break;
            }
        }
    }
    
