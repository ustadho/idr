    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(!udfCekBeforeSave())
            return;
        ar.setId(arId);
        ar.setCustomerId(idRelasi);
        ar.setInvoiceNo(txtNoInvoice.getText());
        ar.setInvoiceDate(jXDatePicker1.getDate());
        ar.setTop(GeneralFunction.udfGetInt(txtTop.getText()));
        ar.setInvoiceAmount(fn.udfGetDouble(txtJumlah.getText()));
        ar.setItemAmount(fn.udfGetDouble(txtJumlah.getText()));
        ar.setOwing(fn.udfGetDouble(txtJumlah.getText()));
        ar.setPaidAmount(new Double(0));
        ar.setTrxType("SAR");
        ar.setJenisBayar("K");
        ar.setIdGudang(new Integer(1));
        ar.setIdSales(salesman.get(cmbSales.getSelectedIndex()).getId_sales());
        try {
            dao.simpanHeader(ar);
            JOptionPane.showMessageDialog(this, arId ==null? "Simpan": "Update" +" Saldo Awal Sukses!");
            if(srcForm instanceof FrmSaldoAwalAR){
                ((FrmSaldoAwalAR)srcForm).udfLoadSaldoAwal();
            }
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(DlgSaldoAwalAr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

