    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            cmbSales.removeAllItems();
            salesman=new SalesDao(conn).cariSemuaData();
            for(Sales s : salesman){
                cmbSales.addItem(s.getNama_sales());
            }
            if(arId!=null){
                String sql="select h.invoice_no, h.id_customer, coalesce(c.nama_relasi,'') as nama, "
                        + "h.invoice_date, h.top, to_Char(h.invoice_date+h.top, 'dd/MM/yyyy') as jth_tempo, \n" +
                            "h.invoice_amount, coalesce(s.nama_sales,'') as nama_sales\n" +
                            "from ar_inv h \n" +
                            "left join m_relasi c on c.id_relasi=h.id_customer\n" +
                            "left join m_salesman s on s.id_sales=h.id_sales\n" +
                            "where id="+arId;
                ResultSet rs=conn.createStatement().executeQuery(sql);
                if(rs.next()){
                    idRelasi=rs.getInt("id_customer");
                    lblRelasi.setText(rs.getString("nama"));
                    txtNoInvoice.setText(rs.getString("invoice_no"));
                    jXDatePicker1.setDate(rs.getDate("invoice_date"));
                    txtTop.setText(rs.getString("top"));
                    lblJthTempo.setText(rs.getString("jth_tempo"));
                    txtJumlah.setText(GeneralFunction.intFmt.format(rs.getDouble("invoice_amount")));
                    cmbSales.setSelectedItem(rs.getString("nama_sales"));
                }
                rs.close();
            }
        } catch (Exception ex) {
            Logger.getLogger(DlgSaldoAwalAr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

